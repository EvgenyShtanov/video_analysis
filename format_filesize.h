#ifndef _FORMAT_FILESIZE_
#define _FORMAT_FILESIZE_

//**************************************************************************
// Format file size
//**************************************************************************
inline QString format_filesize( qint64 s )
{
	if( s < 20*1024 )
	{
		return QString::number( s ) + " b";
	}
	s /= 1024;
	if( s < 20*1024 )
	{
		return QString::number( s ) + " Kb";
	}
	s /= 1024;
	if( s < 20*1024 )
	{
		return QString::number( s ) + " Mb";
	}
	s /= 1024;
	return QString::number( s ) + " Gb";
}

#endif //#ifndef _FORMAT_FILESIZE_
